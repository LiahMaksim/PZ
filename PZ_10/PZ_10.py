#Туристические агентства предлагают следующие туры. Вояж - Мексика,Канада,Израиль, Италия, США. Рейна Тур - Англия, Япония, Канада, ЮАР. Радуга - США,Испания,Швеция, Австралия, Италия, Канада. .Определить:
#в каких турагенствах можно одновременно приобрести туры в Италию и Канаду.
#в турагенство Рейна Тур добавить тур в Индию.
#полный список всех туров.

agencies = {
    'Вояж': {'Мексика', 'Канада', 'Израиль', 'Италия', 'США'},
    'Рейна Тур': {'Англия', 'Япония', 'Канада', 'ЮАР'},
    'Радуга': {'США', 'Испания', 'Швеция', 'Австралия', 'Италия', 'Канада'}
}

agencies_with_both = []
for name, tours in agencies.items():
    if 'Италия' in tours and 'Канада' in tours:
        agencies_with_both.append(name)

agencies['Рейна Тур'].add('Индия')

all_tours = set()
for tours in agencies.values():
    all_tours.update(tours)

print("1. Агентства, где есть и Италия, и Канада:", agencies_with_both)
print("2. Рейна Тур после добавления Индии:", agencies['Рейна Тур'])
print("3. Полный список всех туров:", sorted(all_tours))